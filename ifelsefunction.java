// IF/ELSE FUNCTION.java

public class ifelsefunction {
    public static void main(String[] args) {
        int a, b;
        a = 10;
        b = 20;
        if (a > b) {
            System.out.println("max no is " + a);
        } else {
            System.out.println("max no is " + b);
        }
    }
}
